export function getAllBusinessTypes() {
  const types = [];
  Object.values(BUSINESS_CATEGORIES).forEach(category => {
    category.types.forEach(type => {
      types.push({
        ...type,
        category_code: category.code,
        category_name: category.name
      });
    });
  });
  return types;
}

